# ЧАСТИНА 2. TRY / EXCEPT

# У ЖИТТІ:
# "спробуй зробити"
# якщо не вийде → "оброби помилку"

try:
    x = int(input("Введи число: "))
    print(10 / x)
except:
    print("Сталася помилка")


# ПОЯСНЕННЯ:
# try → пробуємо
# except → якщо щось пішло не так


# ЗАВДАННЯ:
# введи 0 або текст і подивись що буде




# ЧАСТИНА 3. РІЗНІ ТИПИ ПОМИЛОК

# можна обробляти конкретні помилки

try:
    x = int(input("Введи число: "))
    print(10 / x)
except ZeroDivisionError:
    print("Не можна ділити на 0")
except ValueError:
    print("Треба вводити число")


# ЗАВДАННЯ:
# спробуй обидва варіанти помилок




# ЧАСТИНА 4. else (якщо все добре)

