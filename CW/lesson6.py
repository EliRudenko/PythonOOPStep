# ЧАСТИНА 9. ПРИКЛАД З ЖИТТЯ

# банкомат

money = 100

try:
    take = int(input("Скільки зняти? "))

    if take > money:
        raise ValueError("Недостатньо грошей")

    money -= take
    print("Залишилось:", money)

except ValueError as e:
    print("Помилка:", e)


# ЗАВДАННЯ:
# додай перевірку: не можна вводити від’ємне число




# ЧАСТИНА 10. МІНІ-ПРОЄКТ (ГРА)

class Player:
    def __init__(self):
        self.energy = 50

    def work(self):
        self.energy -= 10
        print("Працюєш. Енергія:", self.energy)

    def sleep(self):
        self.energy += 20
        print("Спиш. Енергія:", self.energy)


player = Player()

while True:
    command = input("Введи команду (work/sleep/exit): ")

    try:
        if command == "exit":
            break

        if not hasattr(player, command):
            raise ValueError("Нема такої команди")

        method = getattr(player, command)
        method()

    except ValueError as e:
        print("Помилка:", e)



