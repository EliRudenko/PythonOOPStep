# ЧАСТИНА 4. ОТРИМАТИ АТРИБУТ (getattr)

# у житті:
# "дай мені ім’я"

name = getattr(s, "name")
print(name)


# якщо атрибуту нема — буде помилка

# можна задати значення за замовчуванням
