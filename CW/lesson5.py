# ЧАСТИНА 12. МІНІ-ПРИКЛАД "РОЗУМНИЙ ОБ’ЄКТ"

class Robot:
    def __init__(self):
        self.power = 100

    def work(self):
        print("Робот працює")


r = Robot()

# перевіряємо і викликаємо
if hasattr(r, "work"):
    getattr(r, "work")()


# ФІНАЛЬНИЙ МІНІ-ПРОЄКТ

class Student:
    def __init__(self, name):
        self.name = name
        self.knowledge = 0

    def study(self):
        self.knowledge += 10
        print(self.name, "вчиться")


s = Student("Олег")

# маленька "система команд"
command = "study"

if hasattr(s, command):
    method = getattr(s, command)
    method()


