# ЧАСТИНА 1. ДИВИМОСЬ ЩО Є В ОБ’ЄКТА
class Student:
    def __init__(self, name, age):
        self.name = name
        self.age = age

    def study(self):
        print(self.name, "вчиться")


s = Student("Олег", 15)

# функція dir() показує ВСЕ, що є в об’єкта
print(dir(s))


# ПОЯСНЕННЯ:
# dir() — це як "список всього, що є всередині"


# ЗАВДАННЯ:
# створи клас Dog і подивись dir() для нього



